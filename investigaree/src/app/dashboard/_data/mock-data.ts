// Função para buscar candidaturas por CPF
export function getCandidaturasByCPF(cpf: string) {
  const candidato = CLIENTE_01_CANDIDATOS.find(c => c.cpf === cpf);
  if (!candidato) return [];
  return [{
    cargo: candidato.cargo,
    partido: candidato.partido,
    uf: candidato.uf,
    ano: candidato.ano,
    situacao: candidato.situacao,
    votos: 0,
  }];
}

// Função para buscar doações por CPF
export function getDoacoesByCPF(cpf: string) {
  return CLIENTE_01_DOADORES
    .filter(d => d.cpf === cpf)
    .map(d => ({
      valor: d.valor,
      candidato: d.candidato,
      partido: d.partido,
      ano: d.ano,
    }));
}

// Função para buscar vínculos por CPF
export function getVinculosByCPF(cpf: string) {
  return CLIENTE_01_VINCULOS
    .filter(v => v.cpf === cpf)
    .map(v => ({
      cnpj: v.cnpj,
      razao_social: v.razao_social,
      situacao_cadastral: v.situacao_cadastral,
      qualificacao: v.qualificacao,
    }));
}

// Função para buscar sanções por CPF
export function getSancoesByCPF(cpf: string) {
  const sancionado = CLIENTE_01_SANCIONADOS.find(s => s.cpf === cpf);
  if (!sancionado) return [];
  return [{
    tipo: sancionado.tipo,
    orgao: sancionado.orgao,
    motivo: sancionado.motivo,
    data_inicio: sancionado.dataInicio,
    data_fim: sancionado.dataFim,
  }];
}

// Função para buscar benefícios por CPF
export function getBeneficiosByCPF(cpf: string) {
  return CLIENTE_01_BENEFICIOS
    .filter(b => b.cpf === cpf)
    .map(b => ({
      tipo: b.tipo,
      valor: b.valor,
      ano: b.ano,
    }));
}

// Função para buscar funcionário por CPF com todos os dados relacionados
export function getFuncionarioCompleto(cpf: string) {
  const funcionario = CLIENTE_01_FUNCIONARIOS.find(f => f.cpf === cpf);
  if (!funcionario) return null;

  return {
    funcionario,
    candidaturas: getCandidaturasByCPF(cpf),
    doacoes: getDoacoesByCPF(cpf),
    vinculos: getVinculosByCPF(cpf),
    sancoes: getSancoesByCPF(cpf),
    beneficios: getBeneficiosByCPF(cpf),
  };
}

